using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace Custom
{
    public class Coordinate3D
    {
        public int x;
        public int y;
        public int z;

        public Coordinate3D(int x, int y, int z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
